#!/bin/sh
# migrate-learnings-schema.sh — One-time migration of old-schema learnings.jsonl
# entries to the canonical new schema defined in schemas/learning.schema.json.
#
# Canonical new shape: {ts, step, kind, summary, detail, tags}
#
# Old shape (abandoned): {id, timestamp, category, content, context,
#                         source_task, source_step, promoted}
#
# Field map:
#   timestamp   -> ts
#   source_step -> step
#   category    -> kind
#   content     -> summary
#   context     -> detail
#   (dropped)   -> id, source_task, promoted
#   (new)       -> tags  (defaults to [])
#
# Usage: migrate-learnings-schema.sh [--root <dir>]
#   Walks <root>/.furrow/rows/*/learnings.jsonl automatically.
#   <root> defaults to $PWD.
#
# Idempotency: files whose first record already has a "ts" field are treated
# as already-migrated and skipped (checksum preserved).
#
# Unmappable records: any record that lacks one of [timestamp, source_step,
# category, content, context] is NOT rewritten. The raw record is appended
# with the failure reason to .furrow/rows/<name>/migration-report.md and the
# script exits non-zero (exit 3) after processing every file.
#
# Exit codes:
#   0 — all rows migrated cleanly (or no-ops)
#   1 — usage error / jq not found
#   3 — one or more unmappable records encountered; see migration-report.md(s)

set -eu

_root="${PWD}"
if [ "${1:-}" = "--root" ]; then
  [ -n "${2:-}" ] || { echo "migrate-learnings-schema: --root requires a path" >&2; exit 1; }
  _root="$2"
  shift 2
fi

command -v jq >/dev/null 2>&1 || {
  echo "migrate-learnings-schema: jq not found" >&2
  exit 1
}

_any_unmappable=0

# --- migrate a single file ---
_migrate_file() {
  _file="$1"
  _row_dir="$(dirname "${_file}")"
  _row_name="$(basename "${_row_dir}")"
  _report="${_row_dir}/migration-report.md"

  # Idempotency check: first non-empty line already has .ts?
  _first="$(grep -v '^[[:space:]]*$' "${_file}" | head -n1 || true)"
  if [ -z "${_first}" ]; then
    printf '  skip: %s (empty)\n' "${_file}" >&2
    return 0
  fi

  _has_ts="$(printf '%s' "${_first}" | jq -r 'has("ts")' 2>/dev/null || echo "parse-error")"
  if [ "${_has_ts}" = "true" ]; then
    printf '  skip: %s (already new schema)\n' "${_file}" >&2
    return 0
  fi

  printf '  migrate: %s\n' "${_file}" >&2

  _tmp_out="$(mktemp)"
  _unmappable_in_file=0
  _line_no=0

  # Read each JSON line and map old->new. Use `|| [ -n "$_line" ]` so that
  # a final line without a trailing newline is still processed (otherwise
  # while-read drops it and we silently lose records).
  while IFS= read -r _line || [ -n "${_line}" ]; do
    _line_no=$((_line_no + 1))
    [ -n "${_line}" ] || continue

    # Required old fields check + enum-value check. Any failure makes the
    # record unmappable (spec AC-4 — never silently drop).
    _missing="$(
      printf '%s' "${_line}" | jq -r '
        def valid_step: ["ideate","research","plan","spec","decompose","implement","review"];
        def valid_kind: ["pattern","pitfall","preference","convention","dependency"];
        [
          (if has("timestamp") and (.timestamp | type == "string") and (.timestamp != "") then empty else "timestamp" end),
          (if has("source_step") and (.source_step | type == "string") and (.source_step != "") then empty else "source_step" end),
          (if has("category") and (.category | type == "string") and (.category != "") then empty else "category" end),
          (if has("content") and (.content | type == "string") and (.content != "") then empty else "content" end),
          (if has("context") and (.context | type == "string") and (.context != "") then empty else "context" end),
          (if has("source_step") and ((.source_step // "") | IN(valid_step[]) | not) then "source_step-enum(\(.source_step))" else empty end),
          (if has("category") and ((.category // "") | IN(valid_kind[]) | not) then "category-enum(\(.category))" else empty end)
        ] | join(",")
      ' 2>/dev/null || echo "parse-error"
    )"

    if [ -n "${_missing}" ] && [ "${_missing}" != "" ]; then
      # Log unmappable — do NOT drop.
      if [ ! -f "${_report}" ]; then
        {
          printf '# Learnings migration report — %s\n\n' "${_row_name}"
          printf 'Row: `%s`\n' "${_row_name}"
          printf 'Generated by `bin/frw.d/scripts/migrate-learnings-schema.sh`.\n\n'
          printf '## Unmappable records\n\n'
          printf 'Each entry below could not be migrated from old schema to new schema.\n'
          printf 'Review manually; rewrite by hand if the insight is worth preserving.\n\n'
        } > "${_report}"
      fi
      {
        printf '### line %s (missing: %s)\n\n' "${_line_no}" "${_missing}"
        printf '```json\n%s\n```\n\n' "${_line}"
      } >> "${_report}"
      _unmappable_in_file=$((_unmappable_in_file + 1))
      continue
    fi

    # Map old -> new using jq. Timestamp format is preserved verbatim.
    _mapped="$(
      printf '%s' "${_line}" | jq -c '
        {
          ts: .timestamp,
          step: .source_step,
          kind: .category,
          summary: .content,
          detail: .context,
          tags: ((.tags // []) | if type == "array" then . else [] end)
        }
      ' 2>/dev/null
    )" || _mapped=""

    if [ -z "${_mapped}" ]; then
      if [ ! -f "${_report}" ]; then
        {
          printf '# Learnings migration report — %s\n\n' "${_row_name}"
          printf 'Row: `%s`\n' "${_row_name}"
          printf 'Generated by `bin/frw.d/scripts/migrate-learnings-schema.sh`.\n\n'
          printf '## Unmappable records\n\n'
        } > "${_report}"
      fi
      {
        printf '### line %s (jq-mapping-failed)\n\n' "${_line_no}"
        printf '```json\n%s\n```\n\n' "${_line}"
      } >> "${_report}"
      _unmappable_in_file=$((_unmappable_in_file + 1))
      continue
    fi

    printf '%s\n' "${_mapped}" >> "${_tmp_out}"
  done < "${_file}"

  # Atomically replace the file. If everything was unmappable, still replace
  # with the (possibly empty) mapped output — the report.md preserves the raw
  # dropped records for human review.
  mv "${_tmp_out}" "${_file}"

  if [ "${_unmappable_in_file}" -gt 0 ]; then
    _any_unmappable=1
    printf '    %s unmappable record(s) logged to %s\n' "${_unmappable_in_file}" "${_report}" >&2
  fi
}

# --- walk .furrow/rows/*/learnings.jsonl ---
_rows_glob="${_root}/.furrow/rows"
if [ ! -d "${_rows_glob}" ]; then
  printf 'migrate-learnings-schema: no rows dir at %s\n' "${_rows_glob}" >&2
  exit 0
fi

for _dir in "${_rows_glob}"/*/; do
  [ -d "${_dir}" ] || continue
  _f="${_dir}learnings.jsonl"
  [ -f "${_f}" ] || continue
  _migrate_file "${_f}"
done

if [ "${_any_unmappable}" -ne 0 ]; then
  printf 'migrate-learnings-schema: completed with unmappable records — see migration-report.md in affected rows\n' >&2
  exit 3
fi

exit 0
